    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Double sueldoBasico = Double.parseDouble(david.getText());
        Double diasLaborados = Double.parseDouble(fieldWorkDays.getText());
        Double horasExtras = Double.parseDouble(fieldExtraHours.getText());
        
        Principal operaciones = new Principal();
        
        operaciones.fun_salarioMensual(sueldoBasico, diasLaborados);
        
        fieldSalaryMonth.setText(String.valueOf(operaciones.salarioMensual));
        
        operaciones.fun_horasExtras(sueldoBasico,horasExtras);
        
        fieldExtraHours.setText(String.valueOf(operaciones.horasExtras));
        
        operaciones.fun_auxTransporte();
        
        fieldTransportAux.setText(String.valueOf(operaciones.auxTransporte));
        
        operaciones.fun_TotalDevengado();
        
        fieldTotalDevengado.setText(String.valueOf(operaciones.totalDevengado));
        
        operaciones.fun_salud();
        
        fieldSalud.setText(String.valueOf(operaciones.salud));
        
        operaciones.fun_pension();
        
        fieldPension.setText(String.valueOf(operaciones.pension));
        
        operaciones.fun_otros();
        
        fieldOtros.setText(String.valueOf(operaciones.otros));
        
        operaciones.fun_totalDeducidos();
        
        fieldTotalDeducidos.setText(String.valueOf(operaciones.totalDeducidos));
        
        operaciones.fun_totalPagar();
        
        fieldTotalAll.setText(String.valueOf(operaciones.totalPagar));
    }//GEN-LAST:event_jButton1ActionPerformed


